#Необходимо с помощью срезов выбрать из него города через один (начиная со второго)
# и результат вывести на экран.
c = ["Москва", "Ульяновск", "Самара", "Тверь", "Вологда", "Омск", "Уфа"]
c =c[1::2]
print(c)


