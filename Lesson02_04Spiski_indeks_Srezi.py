a =[9,8,2,4,5]
print(a[:])
# : :  это с самого начала по конец третий это шаг
print((len(a))) # длинна списка
print(a[:5]) # срез по 5 элемент
print(a[4:]) # срез с 4 по конец
print(a[0:-1]) # Срез с 0 по -1 токо -1 не берется
print(a[-1:])

print(a[::-1]) # перевернуть
print(a[::2],'Шаг 2') # С ШАГОМ 2

